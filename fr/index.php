          <h2>COMMENT NE PAS EXCLURE<h2>
          <h1>Les Parents<br/> Artistes</h1>
          <div class="vbar"></div>
          <h3>Quelques recommendations pour les Institutions d’Art et les Résidences d’Artistes</h3>

        </div>
      </div>

      <div class="row introduction">
        <div class="column">

          <h4>Préambule</h4>
          <p>Alors que la responsabilité de la garde des enfants incombe à une écrasante majorité aux mères, nous utilisons le mot ‘parent’ dans ces lignes directrices dans l’espoir que cela puisse changer</p>

          <h4>Remarque préliminaire</h4>
          <p>Traiter l’artiste comme une personne à part entière</p>

          <h4>Une demande</h4>
          <p>Soyez flexible</p>

        </div>
      </div>

      <div class="row statements">
        <div class="column">

          <h4>1.	En tant qu’organisation, soyez explicitement accueillants envers les artistes avec des familles</h4>
          <p>Soyez favorables à l’allaitement, restez en contact avec les artistes lorsqu’ils deviennent parents.</p>
          <p>L’art n’a pas besoin d’être familial, mais l’institution devrait l’être.</p>

          <h4>2.	Faites en sorte que la situation familiale d'un artiste soit prise en compte dès le début d'un projet et que des structures soient mises en place pour accompagner ses responsabilités parentales</h4>
          <p>Il ne devrait pas être laissé à l'artiste d'avoir à `avouer’ être parent, ou craindre de perdre une exposition, une commission ou une résidence s'ils le font.</p>

          <h4>3.	Assumez que tout parent artiste peut avoir besoin de voyager avec son (ses) enfant(s) et un partenaire ou un autre accompagnateur, et prévoyez cela.</h4>

          <h4>4.	Convenez dès le départ avec l’artiste ce que l’on attend de lui/elle, et donnez-lui suffisamment de temps pour qu’il/elle puisse planifier en conséquence</h4>
          <p>Ne faites pas de demandes urgentes de dernière minute pour des textes, des conférences ou autres extras.</p>

          <h4>5.	Envisagez d’avoir un budget spécifique pour les frais de garde d’enfants d’artiste</h4>
          <p>Discutez dès le départ des coûts de garde d'enfants avec l'artiste et indiquez clairement ce que vous pouvez et ne pouvez pas couvrir.</p>
          <p>Permettez aux artistes de facturer la partie de leur cachet qu'ils/elles devront consacrer à la garde d'enfants en tant que coût direct, afin qu'ils ne soient pas imposés comme un revenu.</p>

          <h4>6.	Planifiez les ouvertures et les événements spéciaux qui conviennent le mieux aux parents artistes</h4>
          <p>Envisagez des options de vernissage telles que le brunch du week-end plutôt que de vous en tenir rigoureusement aux débuts de soirée lorsque les enfants ont besoin d'être nourris, lavés et couchés.</p>

          <h4>7.	Soyez conscients des dates des vacances scolaires et programmez autour de celles-ci</h4>
          <p>Offrez aux artistes qui ont besoin de voyager avec des enfants la possibilité d'installer une exposition pendant les congés scolaires par exemple.</p>

          <h4>8.	Repensez ou supprimez les limites d'âge pour les résidences et les prix afin qu'ils incluent les artistes dont la carrière a été interrompue par le fait d'avoir eu et/ou de s'occuper d’enfants</h4>

          <h4>9.	Travaillez avec les artistes pour adapter les résidences à leurs besoins parentaux</h4>
          <p>Cela peut impliquer de permettre à l'artiste de diviser le temps en périodes plus faciles à gérer, ou de les soutenir pendant une période de recherche et de développement dans leur propre studio s'ils/elles ne peuvent pas voyager.</p>

          <h4>10.	Ne lisez pas les lacunes sur un CV pour indiquer un manque d’engagement ou d’effort</h4>
          <p>Les carrières des artistes se présentent sous de nombreuses formes et sont interrompues pour de nombreuses raisons, dont la parentalité.</p>
          <p>Les artistes émergents ne sont pas toujours ceux qui ont obtenu leur diplôme le plus récemment.</p>

