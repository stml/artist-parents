          <h4>Introdução:</h4>
          <p>As mães são as que normalmente tomam a maior parte da responsabilidade por cuidar de filhos, mas neste documento nos referimos a “pais” com a esperança de que isso mude.</p>

          <h4>Sugestão:</h4>
          <p>Trate o artista como uma pessoa íntegra</p>

          <h4>Pedido:</h4>
          <p>Seja flexível</p>

        </div>
      </div>

      <div class="row statements">
        <div class="column">

          <h4>1.	No papel de organização, seja explicitamente bem-vindo a artistas com famílias</h4>
          <p>Compreenda uma mãe que amamenta, fique em contato com artistas quando eles se tornam pais.</p>
          <p>A arte não precisa ser acessível a toda a família, mas a instituição deve ser.</p>

          <h4>2.	Crie uma prática para estabelecer a situação familiar de um artista no início do projeto e se certifique de ter estruturas para acomodar as responsabilidades dos pais</h4>
          <p>Não é responsabilidade do artista ter que "confessar" que é pai ou mãe, e sentir medo de perder uma exposição, comissão ou residência se o fizerem.</p>

          <h4>3.	Suponha que qualquer pai ou mãe artista precise se locomover com seu(s) filho(s) e parceiro/a ou ajudante para cuidar das crianças, e providencie recursos para tal.</h4>

          <h4>4.	Combine com o artista, desde o início de um projeto, o que se espera dele e quando</h4>
          <p>Dê tempo suficiente para que ele possa planejar de acordo: não faça solicitações urgentes de última hora para textos, palestras e afins.</p>

          <h4>5.	Considere ter um orçamento específico para os custos de um artista para com seus filhos</h4>
          <p>Discuta antecipadamente a ajuda de custo destinado aos filhos e seja claro sobre o que você pode ou não cobrir.</p>
          <p>Permita aos artistas faturarem a porcão destinada aos cuidados com os filhos de forma líquida para não caírem em taxas tributarias.</p>

          <h4>6.	Agende “vernissages” e eventos especiais respeitando a rotina dos pais artistas</h4>
          <p>Considere opções como visitas às exposições durante o “brunch” de fim de semana em vez de se limitar rigidamente ao início da noite, quando a maioria dos pais está a serviço da rotina dos filhos.</p>

          <h4>7.	Esteja atento ao ano letivo e programe em torno deste</h4>
          <p>Ofereça aos artistas que precisam levar as crianças ao trabalho, a opção de instalar uma exposição durante feriados ou férias escolares, por exemplo.</p>

          <h4>8.	Repense ou remova os limites de idade para residências, concursos e prêmios para que assim se tornem inclusivos a artistas cujas carreiras foram interrompidas por terem e cuidarem de filhos</h4>

          <h4>9.	Trabalhe com os artistas adaptando as residências para atender às necessidades dos pais</h4>
          <p>Permitindo que o artista divida o período da residência em seções gerenciáveis ​​ou apoiá-los durante um período de pesquisa e desenvolvimento em seu próprio estúdio, se não puderem se locomover com as crianças.</p>

          <h4>10.	Não suponha que lacunas em um currículo indiquem falta de compromisso ou esforço</h4>
          <p>As carreiras dos artistas vêm em muitas formas e são pausadas por muitos motivos, entre eles a paternidade/maternidade.</p>
          <p>Artistas emergentes nem sempre são aqueles que se formaram mais recentemente.</p>

