          <h2>Jak nie wykluczać<h2>
          <h1>Rodziców<br/> Artystów</h1>
          <div class="vbar"></div>
          <h3>Najlepsze praktyki dla instytucji podczas pracy z rodzicami-artystkami</h3>

        </div>
      </div>

      <div class="row introduction">
        <div class="column">

          <p>Chociaż odpowiedzialność za opiekę nad dziećmi spoczywa obecnie w przeważającej mierze na matkach, w niniejszych wytycznych używamy słowa „rodzic”, mając nadzieję, że może się to zmienić.</p>

          <h4>Sugestia ogólna:</h4>
          <p>Traktuj artystkę/tę jako całość</p>

          <h4>Wstępna prośba:</h4>
          <p>Bądź elastyczny</p>

        </div>
      </div>

      <div class="row statements">
        <div class="column">

          <h4>1.	Jako organizacja, otwarcie podchodź do artystów z rodzinami</h4>
          <p>Bądź przyjazny karmieniu piersią, pozostań w kontakcie z artystami, kiedy zostaną rodzicami.</p>
          <p>Sztuka nie musi być przyjazna rodzinie, ale instytucja powinna.</p>

          <h4>2.	Uczyń standardową praktyką ustalenie sytuacji rodzinnej artysty na początku projektu i przygotuj infrastrukturę uwzględniającą ich obowiązki rodzicielskie</h4>
          <p>Nie należy pozostawiać artystom, aby musieli „przyznać się” do bycia rodzicem. Artystka/-ta nie może bać się, że jeśli się do tego przyzna, może stracić wystawę, finansowanie lub rezydencję.</p>

          <h4>3.	Załóż, że każdy rodzic artysta potrzebuje podróżować ze swoim dzieckiem (dziećmi) i partnerem lub innym opiekunem, i zapewnij to.</h4>

          <h4>4.	Na początku ustal z artystą, czego się od niego oczekuje i kiedy. Daj wystarczająco dużo czasu, aby mógł odpowiednio zaplanować swoją pracę</h4>
          <p>Nie stwarzaj nagłej i niezapowiedzianej potrzeby dostarczenia przez artystkę/-tę materiałów na potrzeby realizowanego projektu.</p>

          <h4>5.	Zastanów się nad przygotowaniem określonego budżetu na koszty opieki nad dziećmi artysty.</h4>
          <p>Przedyskutuj z artystą koszty opieki nad dziećmi i wyjaśnij, czego możesz, a czego nie możesz pokryć.</p>

          <h4>6.	Planuj wernisaże i wydarzenia specjalne tak by były wygodne dla rodziców artystów</h4>
          <p>Rozważ opcje, takie jak weekendowy brunch czy prywatne zwiedzanie, zamiast sztywnego trzymania się godzin wczesno wieczornych, kiedy dzieci muszą być karmione, kąpane i kładzione do łóżka.</p>

          <h4>7.	Bierz pod uwagę rozkład zajęć w szkołach</h4>
          <p>Być może artystce wygodniej będzie współpracować w czasie ferii lub wakacji szkolnych.</p>

          <h4>8.	Ponownie przemyśl lub usuń ograniczenia wiekowe dla rezydencji i nagród, tak aby obejmowały artystki, których kariera została przerwana przez posiadanie dzieci i opiekę nad nimi</h4>

          <h4>9.	Poświęć uwagę by wspólnie z artystką dostosować plan rezydencji do ich potrzeb rodzicielskich.</h4>
          <p>Może to oznaczać umożliwienie artystce podzielenia czasu trwania rezydencji na kilka, krótszych okresów, które są łatwiejsze do zorganizowania. Inna możliwość to wspieranie artystki w fazie badań i poszukiwań w jej własnej pracowni, jeśli nie może podróżować.</p>

          <h4>10.	Nie odczytuj luk w CV, jako wskaźników braku zaangażowania lub wysiłku</h4>
          <p>Kariera artystów przybiera różne kształty i jest wstrzymywana z wielu powodów. Jednym z takich powodów jest rodzicielstwo.</p>
          <p>Wschodzący artyści nie zawsze są tymi, którzy ukończyli ostatnio studia. </p>

