      <div class="row statements">
        <div class="column">

          <h4>1.	As an organisation, be explicitly welcoming to artists with families</h4>
          <p>Be breastfeeding friendly, stay in contact with artists when they become parents.</p>
          <p>The art does not need to be family friendly, but the institution should be.</p>

          <h4>2.	Make it standard practice to establish an artist’s family circumstances at the outset of a project, and have structures in place to accommodate their parenting responsibilities</h4>
          <p>It shouldn’t be left to the artist to have to ‘confess’ to being a parent, or to fear they may lose a show, commission or residency if they do so.</p>

          <h4>3.	Assume that any artist parent may need to travel with their children and a partner or other caregiver, and provide for this.</h4>

          <h4>4.	Agree with the artist at the outset what is expected of them and when, and give enough lead time so that they can plan accordingly</h4>
          <p>Don’t make urgent last-minute requests for texts, talks, and other extras.</p>

          <h4>5.	Consider having a specific budget for an artist’s childcare costs</h4>
          <p>Discuss childcare costs upfront with the artist and be clear about what you can and can’t cover.</p>
          <p>Allow artists to invoice for that portion of their fee that they will need to spend on childcare as a direct cost so that they won’t be taxed for it as income.</p>

          <h4>6.	Schedule openings and special events as convenient to artist parents</h4>
          <p>Consider options such as weekend daytime private views rather than sticking rigidly to early evenings when children need to be fed, bathed and put to bed.</p>

          <h4>7.	Be aware of term dates and program around them</h4>
          <p>Offer artists who need to travel with children the option of installing a show over half term, for example.</p>

          <h4>8.	Re-think or remove age limits for residencies and awards so that they become inclusive of artists whose careers have been interrupted by having and caring for children</h4>

          <h4>9.	Work with artists to adapt residencies to fit around their parenting needs</h4>
          <p>This may involve allowing the artist to split the residency period into more manageable sections, or supporting them through a research and development period in their own studio if they can’t travel.</p>

          <h4>10.	Don’t read gaps on a CV to indicate a lack of commitment or effort</h4>
          <p>Artists’ careers come in many shapes, and are paused for many reasons, parenting among them.</p>
          <p>Emerging artists are not always those who graduated most recently.</p>

