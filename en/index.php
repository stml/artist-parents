          <h4>An introductory statement:</h4>
          <p>While responsibility for childcare currently falls overwhelmingly on mothers, we are using the word ‘parent’ in these guidelines in the hope that this may change.</p>

          <h4>An introductory suggestion:</h4>
          <p>Treat the artist as a whole person</p>

          <h4>An introductory request:</h4>
          <p>Be flexible</p>

